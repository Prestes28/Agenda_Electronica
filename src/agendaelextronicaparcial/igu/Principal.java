    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int indi = 0;
        String indice = txtIndice.getText();
        indi = Integer.parseInt(indice);
        tareas[indi] = txtTarea.getText();
        fechas[indi] = txtFecha.getText();
        descripciones[indi]= txtDescripcion.getText();
        int cont = 0;
        for(int i = 0; i< tareas.length; i++){
            if(tareas[i]==null){
            cont++;
            } 
        }
        System.out.println(cont);
        String libres = String.valueOf(cont);
        txtLibres.setText(libres);
        if(cont==0){
                JOptionPane.showMessageDialog(this,"Agenda completada");
            }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTxtActionPerformed
        FileWriter archivo = null ;
        PrintWriter escritor = null ;
        
        try{
            archivo = new FileWriter("C://AgendaElectronica.txt");
            escritor = new PrintWriter(archivo);
            
            for(int i=0;i<10; i++){
                try{
                escritor.println(tareas[i]);
                }catch(Exception e){
                    System.out.println("No se pudo escribir el indice "+i);
                }
            }
            
        }catch(Exception e){
            System.out.println("Error: "+e.getMessage());
        }finally{
            try {
                archivo.close();
            } catch (IOException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btnTxtActionPerformed

 
